<template>
  <section>
    <h2>Friends list</h2>
    <ul>
      <friend-contact
        v-for="friend in friends"
        :key="friend.id"
        :id="friend.id"
        :name="friend.name"
        :phone-number="friend.phone"
        :email-address="friend.email"
        :is-favourite="friend.isFavourite"
        @toggle-favourite="toggleFavourite"
      ></friend-contact>
    </ul>
  </section>
</template>

<script>
export default {
  data() {
    return {
      friends: [
        {
          id: "shiva",
          name: "shiva sai",
          phone: "0123 456 789",
          email: "shiva@gmail.com",
          isFavourite: "0",
        },
        {
          id: "sai",
          name: "sai",
          phone: "0123 456 789",
          email: "sai@gmail.com",
          isFavourite: "0",
        },
      ],
    };
  },
  methods: {
    toggleFavourite(id) {
      const identifiedFriend = this.friends.find(
        (eachItem) => eachItem.id === id
      );
      identifiedFriend.isFavourite =
        identifiedFriend.isFavourite === "0" ? "1" : "0";
    },
  },
};
</script>

